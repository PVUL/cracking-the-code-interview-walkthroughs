mocha.setup("bdd");
const { assert } = chai;

describe("Sorted Merge", () => {
  it("works", () => {
    const a = [2, 3, 4, 5, 6, 8, 10, 100, 0, 0, 0, 0, 0, 0];
    const b = [1, 4, 6, 7, 7, 7];
    sortedMerge(a, b, 8, 6);

    assert.equal(a.join(""), "12344566777810100");
    // [1, 2, 3, 4, 4, 5, 6, 6, 7, 7, 7, 8, 10, 100]
  });
});

