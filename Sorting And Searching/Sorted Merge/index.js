/* 
Given sorted arrays A and B, where A has a large enough buffer at the end to
hold B, merge B into A in sorted order.

Example
Input
a = [2, 3, 4, 5, 6, 8, 10, 100, 0, 0, 0, 0, 0, 0];
b = [1, 4, 6, 7, 7, 7];
Output
    [1, 2, 3, 4, 4, 5, 6, 6, 7, 7, 7, 8, 10, 100]
*/

function sortedMerge(a, b, lastA, lastB) {
  let indexA = lastA - 1;
  let indexB = lastB - 1;
  let indexMerged = lastA + lastB - 1;

  while (indexB >= 0) {
    if (indexA >= 0 && a[indexA] > b[indexB]) {
      a[indexMerged] = a[indexA];
      indexA--;
    } else {
      a[indexMerged] = b[indexB];
      indexB--;
    }
    indexMerged--;
  }
}

